<p>
Las personas encargadas de alimentar el sistema son las áreas sustantivas del SENASICA (Salud Animal, Sanidad Acuícola y Pesquera, Sanidad Vegetal, Inocuidad Agroalimentaria, Acuícola y Pesquera, Inspección Fitozoosanitaria), que generan información de primera mano, y la Dirección de Planeación e Inteligencia Sanitaria que monitorean a diario noticias internacionales.
