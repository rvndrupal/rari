<h2>¿QUÉ ES RARI?</h2>

<p>
Es una plataforma informática que permite el intercambio rápido y oportuno de datos, para generar información estratégica de inteligencia, con diferentes niveles de acceso, para la toma de 
decisiones de la alta Dirección, a través del flujo interno de comunicados relevantes relacionados con: alertas, noticias, cuarentenas, retenciones, información oficial, así como innovaciones 
científicas y tecnológicas en materia de sanidades. 
</p>

<h2>ANTECEDENTES</h2>

<p>
La Red de Alerta Rápida Interna (RARI) surge en el Marco del Proyecto de Facilitación del Tratado de Libre Comercio entre México y la Unión Europea (PROTLCUEM).
</p>

<h2>ALCANCES DE LA RED DE ALERTA RAPIDA INTERNA (RARI)</h2>

<ul>
<li>Primera plataforma en México, en materia de sanidad agropecuaria, acuícola y pesquera, la cual a través del monitoreo pretende reducir los riesgos derivados del: intercambio comercial, condiciones geográficas, climatológicas, turismo, migración y bioterrorismo.
</li>
<li>
Contar una red integral de información sanitaria al interior del SENASICA, mediante la coordinación de acciones entre las áreas, con el propósito de contrarrestar y controlar riesgos.</li>
<li>
Contribuir a la mitigación de riesgos a nivel nacional, a través de la comunicación oportuna de eventos.
</li>
<li>
Conocer oportunamente innovaciones científicas y tecnológicas que aporten elementos para la mejora de las sanidades.
</li>
<li>
Generar informes estadísticos que permitan visualizar e informar el comportamiento de los riesgos que se emiten a nivel nacional e internacional, así como de las actividades que se realizan al 
interior del SENASICA.
</li>
</ul>

